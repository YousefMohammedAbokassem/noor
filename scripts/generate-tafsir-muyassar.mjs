import { writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';

const TRANSLATION_KEY = 'arabic_moyassar';
const OUTPUT_FILE = resolve(process.cwd(), 'src/features/quran/data/tafsirMuyassar.generated.ts');
const SOURCE_LABEL = 'التفسير الميسر';
const TITLE_LABEL = 'معنى الآية';

const normalizeText = (value) =>
  String(value ?? '')
    .replace(/\s+/g, ' ')
    .replace(/\u00A0/g, ' ')
    .trim();

const fetchSurah = async (surahNumber) => {
  const url = `https://quranenc.com/api/v1/translation/sura/${TRANSLATION_KEY}/${surahNumber}`;
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Unable to fetch surah ${surahNumber}. HTTP ${response.status}`);
  }

  const payload = await response.json();
  if (!Array.isArray(payload?.result)) {
    throw new Error(`Unexpected payload for surah ${surahNumber}`);
  }

  return payload.result;
};

const run = async () => {
  const entries = {};

  for (let surah = 1; surah <= 114; surah += 1) {
    const result = await fetchSurah(surah);
    for (const item of result) {
      const surahId = Number(item?.sura);
      const ayah = Number(item?.aya);
      const text = normalizeText(item?.translation);
      if (!surahId || !ayah || !text) continue;

      entries[`${surahId}:${ayah}`] = {
        source: SOURCE_LABEL,
        title: TITLE_LABEL,
        text,
      };
    }
  }

  const generatedAt = new Date().toISOString();
  const fileContent = `/* eslint-disable */
// Auto-generated by scripts/generate-tafsir-muyassar.mjs
// Source: QuranEnc API (${TRANSLATION_KEY})
// Generated at: ${generatedAt}

import { LocalVerseTafsirData } from './localTafsir';

export const tafsirMuyassarGeneratedEntries: Record<string, LocalVerseTafsirData> = ${JSON.stringify(
    entries,
    null,
    2,
  )};
`;

  await writeFile(OUTPUT_FILE, fileContent, 'utf8');
  console.log(`Generated ${Object.keys(entries).length} tafsir entries -> ${OUTPUT_FILE}`);
};

run().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
